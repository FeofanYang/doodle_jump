cc.Class({
    extends: cc.Component,

    onCollisionEnter: function(other, self) {
        var target = other.name.split('<')[0];
        if(target == 'player'){
            console.log('game over!');
            cc.director.loadScene('game_scene');
        }else if(target == 'board'){
            other.node.removeFromParent();
        }
    },
});
